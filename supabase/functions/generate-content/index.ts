import "https://deno.land/x/xhr@0.1.0/mod.ts";
import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { type, topic, details } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get('LOVABLE_API_KEY');
    
    if (!LOVABLE_API_KEY) {
      throw new Error('LOVABLE_API_KEY is not configured');
    }

    let systemPrompt = '';
    let userPrompt = '';

    if (type === 'video-script') {
      systemPrompt = 'You are a professional video script writer. Create engaging, well-structured video scripts with clear sections for intro, main content, and outro. Include visual cues and timing suggestions.';
      userPrompt = `Create a video script about: ${topic}\nAdditional details: ${details}`;
    } else if (type === 'podcast') {
      systemPrompt = 'You are a podcast content creator. Write engaging podcast episode scripts with natural conversation flow, including host dialogue, guest questions, and segment breaks.';
      userPrompt = `Create a podcast episode script about: ${topic}\nAdditional details: ${details}`;
    } else if (type === 'article') {
      systemPrompt = 'You are a professional content writer. Create well-researched, SEO-optimized articles with clear structure, compelling headlines, and engaging content.';
      userPrompt = `Write an article about: ${topic}\nAdditional details: ${details}`;
    }

    const response = await fetch('https://ai.gateway.lovable.dev/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Authorization': `Bearer ${LOVABLE_API_KEY}`,
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        model: 'google/gemini-2.5-flash',
        messages: [
          { role: 'system', content: systemPrompt },
          { role: 'user', content: userPrompt }
        ],
      }),
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error('Lovable AI error:', response.status, errorText);
      throw new Error(`Lovable AI request failed: ${response.status}`);
    }

    const data = await response.json();
    const content = data.choices[0].message.content;

    return new Response(JSON.stringify({ content }), {
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
    });
  } catch (error) {
    console.error('Error in generate-content function:', error);
    return new Response(JSON.stringify({ error: error.message }), {
      status: 500,
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
    });
  }
});
